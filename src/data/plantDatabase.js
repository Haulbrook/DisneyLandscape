export const PLANT_DATABASE = {
  grasses: GRASSES,
  groundcovers: GROUND_COVERS,
  perennials: PERENNIALS,
  shrubs: SHRUBS,
  trees: TREES,
};

// Flatten for easy access
export const ALL_PLANTS = [
  ...GRASSES,
  ...GROUND_COVERS,
  ...PERENNIALS,
  ...SHRUBS,
  ...TREES,
];

// Get all unique sizes available for a plant
export const getPlantSizes = (plantId) => {
  const plant = ALL_PLANTS.find(p => p.id === plantId);
  return plant?.sizes || ['1gal'];
};

// Get size multipliers for spread calculation
export const getSizeMultiplier = (size) => {
  const normalized = size.toLowerCase().replace(/\s+/g, '').replace(/'|"/g, '');
  return SIZE_MULTIPLIERS[normalized] || SIZE_MULTIPLIERS['3gal'];
};

export default PLANT_DATABASE;
